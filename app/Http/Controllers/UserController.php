    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        // Create new user
        $new_user = new User;
        $new_user->name = $request->get('name');
        $new_user->user_name = $request->get('user_name');
        $new_user->avatar = $request->get('avatar');
        $new_user->email = $request->get('email');
        $new_user->user_role = $request->get('user_role');
        $new_user->password = bcrypt( trim( $request->get('password') ) );
        $new_user->api_token = $request->get('api_token');

        return $new_user->save() ? 'Account created!' : 'Unable to create account';
    }

